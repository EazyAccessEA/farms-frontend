// 🎯 COLOR UTILITIES - Helper functions
export const ColorUtils = {
  // Get gradient CSS
  getGradient: (gradientName: keyof typeof SiliconValleyColors.gradients) => {
    const gradient = SiliconValleyColors.gradients[gradientName];
    return `linear-gradient(135deg, ${gradient.from} 0%, ${gradient.to} 100%)`;
  },

  // Get emotion color
  getEmotion: (emotion: keyof typeof SiliconValleyColors.emotions) => {
    return SiliconValleyColors.emotions[emotion];
  },

  // Get semantic color
  getSemantic: (category: keyof typeof SiliconValleyColors.semantic, type: string) => {
    return SiliconValleyColors.semantic[category][type as keyof typeof SiliconValleyColors.semantic[typeof category]];
  },

  // Check contrast ratio
  getContrastRatio: (color1: string, color2: string) => {
    // Simplified contrast calculation
    const hex1 = color1.replace('#', '');
    const hex2 = color2.replace('#', '');
    
    const r1 = parseInt(hex1.substr(0, 2), 16);
    const g1 = parseInt(hex1.substr(2, 2), 16);
    const b1 = parseInt(hex1.substr(4, 2), 16);
    
    const r2 = parseInt(hex2.substr(0, 2), 16);
    const g2 = parseInt(hex2.substr(2, 2), 16);
    const b2 = parseInt(hex2.substr(4, 2), 16);
    
    const luminance1 = (0.299 * r1 + 0.587 * g1 + 0.114 * b1) / 255;
    const luminance2 = (0.299 * r2 + 0.587 * g2 + 0.114 * b2) / 255;
    
    const ratio = (Math.max(luminance1, luminance2) + 0.05) / (Math.min(luminance1, luminance2) + 0.05);
    return ratio;
  },

  // Validate accessibility
  isAccessible: (foreground: string, background: string, level: 'AA' | 'AAA' = 'AA') => {
    const ratio = ColorUtils.getContrastRatio(foreground, background);
    return level === 'AA' ? ratio >= 4.5 : ratio >= 7;
  },
};

